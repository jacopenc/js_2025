console.info('Consola de Información');       // Información
console.debug('Consola de Depuración');       // Depuración (a veces oculta por defecto en navegadores)
console.warn('Consola de Aviso ⚠️');          // Advertencias
console.error('Consola de Error ❌');         // Errores
