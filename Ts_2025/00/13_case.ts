const fruta = "manzana";

switch (fruta) {
  case "manzana":
    console.log("Es una manzana");
    break;
  case "banana":
    console.log("Es una banana");
    break;
  default:
    console.log("Fruta desconocida");
}

const numero = 2;

switch (numero) {
  case 1:
    console.log("Uno");
    break;
  case 2:
    console.log("Dos");
    break;
  default:
    console.log("Otro número");
}
